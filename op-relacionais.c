#include <stdio.h>
int main(){
    int valor1 = 15;
    int valor2 = 10;

    printf("Maior que: %d\n", valor1 > valor2);
    printf("Menor que: %d\n", valor1 < valor2);
    printf("Maior ou igual: %d\n", valor1 >= valor2);
    printf("Menor ou igual: %d\n", valor1 <= valor2);
    printf("Igual a: %d\n", valor1 == valor2);
    printf("Diferete de: %d\n", valor1 != valor2);
}